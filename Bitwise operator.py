num1 = 42
num2 = 3
print("num1&num2=",num1 & num2)  # AND operator
print("\nnum1 num2=",num1 | num2)  # OR operator
print("\n~num1=", ~num1)  # NOT operator
print("\nnum1^num2=", num1 ^ num2)  # XOR operator
print("\nnum1<<2=", num1 << 2)  # Left Shift operator
print("\nnum2>>2=", num2 >> 2)  # Right Shift operator